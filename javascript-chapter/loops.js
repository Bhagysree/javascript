// loops : iteraing element
// there are 3 loops
// 1. for loop
// 2. while loop    
// 3. do while loop

// for loop : used to iterate over a block of code a number of times
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// while loop : used to execute a block of code as long as a specified condition is true
let j = 1;
while (j <= 10) {
    console.log(j);
    j++;
}

// do while loop : similar to while loop, but it executes the block of code once before checking the condition
let k = 1;
do {
    console.log(k);
    k++;
} while (k <= 10);