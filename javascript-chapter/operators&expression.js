// operators:


// Arithmetic Operators:
let a = 10;
let b = 9;
console.log("a + b = ", a + b); // Addition
console.log("a - b = ", a - b); // Subtraction
console.log("a * b = ", a * b); // Multiplication
console.log("a / b = ", a / b); // Division
console.log("a % b = ", a % b); // Modulus
console.log("a ** b = ", a ** b); // Exponentiation
console.log("a++ = ", a++); // Post-increment
console.log("b-- = ", b--); // Post-decrement


//assignment Operators:

let assignment = 10;
assignment += 5; // Equivalent to assignment = assignment + 5
console.log(assignment);
assignment -= 3; // Equivalent to assignment = assignment - 3
console.log(assignment);
assignment *= 2; // Equivalent to assignment = assignment * 2
console.log(assignment);
assignment /= 4; // Equivalent to assignment = assignment / 4

console.log(assignment);
assignment %= 3; // Equivalent to assignment = assignment % 3
console.log(assignment);
assignment **= 2; // Equivalent to assignment = assignment ** 2
console.log(assignment);
assignment <<= 1; // Equivalent to assignment = assignment << 1
console.log(assignment);

// Comparison Operators:
let com1 = 5;
let com2 = 10;
console.log("com1 == com2: ", com1 == com2); // Equality
console.log("com1 != com2: ", com1 != com2); // Inequality
console.log("com1 === com2: ", com1 === com2); // Strict Equality
console.log("com1 !== com2: ", com1 !== com2); // Strict Inequality

// logical Operators:
let x = 5;
let y = 10;
console.log(x>y && x==5); // Logical AND
console.log(x>y || x==5); // Logical OR
console.log(!false); // Logical NOT
console.log(!true);

// Bitwise Operators:the left side is the first operand and the right side is the second operand.
let bit1 = 5; // 0101 in binary
let bit2 = 3; // 0011 in binary
console.log("bit1 & bit2: ", bit1 & bit2); // Bitwise AND

